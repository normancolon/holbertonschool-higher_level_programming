#!/usr/bin/python3

def add(number1, number2):
    """My addition function

    Args:
        number1: first integer
        number2: second integer

    Returns:
        The return value. number1 + number2
    """
    return (number1 + number2)


def sub(minuend, subtrahend):
    """My subtraction function

    Args:
        minuend: first integer
        subtrahend: second integer

    Returns:
        The return value. minuend - subtrahend
    """
    return (minuend - subtrahend)


def mul(multiplicand, multiplier):
    """My multiplication function

    Args:
        multiplicand: first integer
        multiplier: second integer

    Returns:
        The return value. multiplicand * multiplier
    """
    return (multiplicand * multiplier)


def div(dividend, divisor):
    """My division function

    Args:
        dividend: first integer
        divisor: second integer

    Returns:
        The return value. dividend / divisor
    """
    return int(dividend / divisor)

